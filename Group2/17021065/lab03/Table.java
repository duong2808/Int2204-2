package tuan2;

public class Table {
	private String mat; // vat lieu cau thanh
	private int foot; // so chan ban
	private double S; // dien tich mat ban
	public String getMat() {
		return mat;
	}
	public void setMat(String mat) {
		this.mat = mat;
	}
	public int getFoot() {
		return foot;
	}
	public void setFoot(int foot) {
		this.foot = foot;
	}
	public double getS() {
		return S;
	}
	public void setS(double s) {
		S = s;
	}
}
