package tuan2;

public class Teacher {
	private String name;
	private int exp; // kinh nghiem lam viec (year)
	private String subject; // mon hoc giang day
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getExp() {
		return exp;
	}
	public void setExp(int exp) {
		this.exp = exp;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
}
