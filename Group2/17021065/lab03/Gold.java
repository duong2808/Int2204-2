package tuan2;

public class Gold {
	private double purity; // do tinh khiet (%)
	private String type; // loai vang
	private double weight; // trong luong (g)
	public double getPurity() {
		return purity;
	}
	public void setPurity(double purity) {
		this.purity = purity;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public double getWeight() {
		return weight;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
}
