package tuan2;

public class Diamond {
	private double purity; // do tinh khiet (%)
	private double size; // kich co (mm)
	private double weight; // trong luong (carat)
	public double getPurity() {
		return purity;
	}
	public void setPurity(double purity) {
		this.purity = purity;
	}
	public double getSize() {
		return size;
	}
	public void setSize(double size) {
		this.size = size;
	}
	public double getWeight() {
		return weight;
	}
	public void setWeight(double weight) {
		this.weight = weight;
	}
}
