package lab03.java;

public class Student {
	private String name;
	private int age;
	private String add;
	private String CMND;
	private String MSV;
	private int Group;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getAdd() {
		return add;
	}
	public void setAdd(String add) {
		this.add = add;
	}
	public String getCMND() {
		return CMND;
	}
	public void setCMND(String cMND) {
		CMND = cMND;
	}
	public String getMSV() {
		return MSV;
	}
	public void setMSV(String mSV) {
		MSV = mSV;
	}
	public int getGroup() {
		return Group;
	}
	public void setGroup(int group) {
		Group = group;
	}

}
