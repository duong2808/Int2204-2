package chap05.oop;

public class SanhOrange extends Orange{
	private String size;
	private int cost ;
	private String color ;
	public String getSize() {
		return this.size;
	}
	public void setSize(String s) {
		this.size = s;
	}
	public int getCost() {
		return this.cost;
	}
	public void setCost(int s) {
		this.cost = s;
	}
	public String getColor() {
		return this.color;
	}
	public void setColor(String s) {
		this.color = s;
	}
}
