package baitap;

public class Dog {
	private String HouseDog;
	private String PoliceDog;
	private String Dog;
	
	public String getHouseDog() {
		return HouseDog;
	}
	public void setHouseDog(String houseDog) {
		HouseDog = houseDog;
	}
	public String getPoliceDog() {
		return PoliceDog;
	}
	public void setPoliceDog(String policeDog) {
		PoliceDog = policeDog;
	}
	public String getDog() {
		return Dog;
	}
	public void setDog(String dog) {
		Dog = dog;
	}
	
	
}
